function formvalidate() {
    var name = document.getElementById("uname").value
    var email = document.getElementById("uemail").value
    var subject = document.getElementById("usubject").value
    var message = document.getElementById("umessage").value

    if (name == "") {
        document.getElementById("errorname").innerHTML = "Name field is Required*"
    }

    else if (email == "") {
        document.getElementById("erroremail").innerHTML = "Email field is Required*"
        document.getElementById("errorname").innerHTML = ""

    }

    else if (subject == "") {
        document.getElementById("errorsubject").innerHTML = "Subject field is Required*"
        document.getElementById("erroremail").innerHTML = ""
    }

    else if (message == "") {
        document.getElementById("errormessage").innerHTML = "Message field is Required*"
        document.getElementById("errorsubject").innerHTML = ""
    }

    else if (name != "" && email != "" && subject != "" && message != "") {
        document.getElementById("msg").style.display = "block"
        var name = document.getElementById("uname").value = ''
        var email = document.getElementById("uemail").value = ''
        var subject = document.getElementById("usubject").value = ''
        var message = document.getElementById("umessage").value = ''
    }
    else {
        document.getElementById("errormessage").innerHTML = ""
    }
}

function nameevent() {
    var name = document.getElementById("uname").value


    if (name != "") {
        document.getElementById("errorname").innerHTML = ""

    }
}

function emailevent() {
    var email = document.getElementById("uemail").value


    if (email != "") {
        document.getElementById("erroremail").innerHTML = ""

    }
}

function subjectevent() {
    var subject = document.getElementById("usubject").value


    if (subject != "") {
        document.getElementById("errorsubject").innerHTML = ""

    }
}

function messageevent() {
    var message = document.getElementById("umessage").value


    if (message != "") {
        document.getElementById("errormessage").innerHTML = ""

    }
}
